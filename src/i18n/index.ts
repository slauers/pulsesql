export function getInitialLocale(): AppLocale {
  try {
    const raw = localStorage.getItem('pulsesql-system-config');
    if (raw) {
      const parsed = JSON.parse(raw) as { ui?: { locale?: AppLocale } };
      if (parsed.ui?.locale === 'en-US' || parsed.ui?.locale === 'pt-BR') {
        return parsed.ui.locale;
      }
    }
  } catch {
    // Ignore malformed config and fall back below.
  }

  return navigator.language.toLowerCase().startsWith('en') ? 'en-US' : 'pt-BR';
}

export function translate(locale: AppLocale, key: TranslationKey, params?: TranslationParams) {
  const template = messages[locale][key] ?? messages['pt-BR'][key];
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, token: string) => {
    const value = params[token];
    return value == null ? '' : String(value);
  });
}

export function formatDateTime(locale: AppLocale, value: string) {
  const timestamp = Number(value);
  if (Number.isFinite(timestamp)) {
    return new Date(timestamp).toLocaleString(locale);
  }

  return value;
}

export function formatNumber(locale: AppLocale, value: number) {
  return new Intl.NumberFormat(locale).format(value);
}
